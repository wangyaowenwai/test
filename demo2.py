# 转义字符
print('hello\nword')  # \n 表示换行 a=== newline

print('hello\tworld')

print('helloooo\tworld')

print('hello\rworld')  # world 对hello进行两两覆盖

print('hello\bworld')  # \b 是退一格 变成 hellworld

print('https:\\\\www.baidu.com')  # 有两个 \\ 的时候会当做一个\来输出

print('我说的:\'耶稣都救不了你\'')  # 输出结果 字符串中输出的内容

